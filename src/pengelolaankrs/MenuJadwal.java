    public ArrayList<Jadwal> getJadwalList(String keyword){
     ArrayList<Jadwal> jadwalList = new ArrayList<Jadwal>();
     Koneksi koneksi =  new Koneksi();
     Connection connection = koneksi.getKoneksi();
     
     String query ="SELECT * FROM tb_jadwal"+keyword;
     Statement st;
     ResultSet rs;
     
     try{
         st = connection.createStatement();
         rs = st.executeQuery(query);
         while (rs.next()){
             jadwal = new Jadwal(
                     rs.getString("kode_jadwal"),
                     rs.getString("makul"),
                     rs.getString("dosen"),
                     rs.getString("kelas"),
                     rs.getString("Semester"),
                     rs.getString("jam"),
                     rs.getString("hari")
             );
             jadwalList.add(jadwal);
         }
     } catch (SQLException | NullPointerException ex){
         System.err.println("Koneksi Null Gagal");
     }
        return jadwalList;
    }
    
    public void selectJadwal(String keyword){
        ArrayList<Jadwal> list = getJadwalList(keyword);
        DefaultTableModel model = (DefaultTableModel)tJadwal.getModel();
        Object[] row = new Object[8];
        
        for (int i = 0; i <list.size(); i++){
            row[0] = list.get(i).getMakul();
            row[1] = list.get(i).getKodeJadwal();
            row[2] = list.get(i).getDosen();
            row[3] = list.get(i).getKelas();
            row[4] = list.get(i).getSemester();
            row[5] = list.get(i).getJam();
            row[6] = list.get(i).getHari();
            model.addRow(row);
        }
    }

    public final void resetTable(String keyword){
        DefaultTableModel model = (DefaultTableModel)tJadwal.getModel();
        model.setRowCount(0);
        selectJadwal(keyword);
    }
    
