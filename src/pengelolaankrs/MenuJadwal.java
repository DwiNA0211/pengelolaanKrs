    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (e1.getText().equals("") ||
            e2.getText().equals("")) 
        { 
            JOptionPane.showMessageDialog(null, "Pilih data yang ingin diubah");
        }else {
            qry = "UPDATE tb_jadwal SET makul=?, dosen=?, kelas=?, "
                    + "semester=?, jam=?, hari=? WHERE kode_jadwal=?";
            
            try {
                Koneksi koneksi = new Koneksi();
                Connection con = koneksi.getKoneksi();
                PreparedStatement ps;
                ps = con.prepareStatement(qry);
                ps.setString(1, e1.getText());
                ps.setString(2, e3.getText());
                ps.setString(3, e4.getText());
                ps.setString(4, e5.getText());
                ps.setString(5, e6.getText());
                ps.setString(6, e7.getText());
                ps.setString(7, e2.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Berhasil mengubah data", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
                resetTable("");
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Gagal mengubah data: " + e.getMessage(), "Pemberitahuan", JOptionPane.ERROR_MESSAGE);
                System.err.println("gagal ubah data: " + e.getMessage());
            }
        }
        resetTable("");
        batal();
    }//GEN-LAST:event_jButton2ActionPerformed

