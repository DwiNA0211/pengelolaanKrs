    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        try{
            Koneksi koneksi = new Koneksi();
            Connection con = koneksi.getKoneksi();
            PreparedStatement ps;
                String executeQuery =  "Insert into tb_krs"
                        + "(id_krs,npm,kode_jadwal,tahun_ajar,nama,dosen,Semester,makul) value(?,?,?,?,?,?,?,?)";
                ps = con.prepareStatement(executeQuery);
                ps.setString(1, id.getText());
                ps.setString(2, npm.getText());
                ps.setString(3, nama.getText());
                ps.setString(4, tahun.getText());
                ps.setString(5, semester.getSelectedItem().toString());
                ps.setString(6, cbMakul.getSelectedItem().toString());
                ps.setString(7, dosen.getText());
                ps.setString(8, kodejadwal1.getText());
            ps.executeUpdate();
        }catch(SQLException ex){
            System.err.println(ex);
        }
        resetTable("");
        batal();
    }//GEN-LAST:event_simpanActionPerformed

