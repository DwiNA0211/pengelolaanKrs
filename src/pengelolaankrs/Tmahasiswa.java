    private void bOk1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bOk1ActionPerformed
        // TODO add your handling code here:
        con = Koneksi.getKoneksi();
        String dsn = "";
        String kodeJadwal = "";
        
        try {
            con = Koneksi.getKoneksi();
            st = con.createStatement();
            
            // query makul
            rs = st.executeQuery("SELECT * FROM tb_jadwal WHERE makul LIKE '%" + 
                    cbMakul.getSelectedItem().toString() + "%'");
            
            while (rs.next()){
                dsn = rs.getString("dosen");
                kodeJadwal = rs.getString("kode_jadwal");
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Terjadi Kesalahan: " + e.getMessage());
        }
        
        dosen.setText(dsn);
        kodejadwal1.setText(kodeJadwal);
    }//GEN-LAST:event_bOk1ActionPerformed

