    public void setComboMakul() {
        try {
            con = Koneksi.getKoneksi();
            st = con.createStatement();
            String makul = "";
            
            // query makul
            rs = st.executeQuery("SELECT * FROM tb_jadwal WHERE semester LIKE '%" + 
                    semester.getSelectedItem().toString() + "%'");
            
            while (rs.next()){
                makul = rs.getString("makul");
                cbMakul.addItem(makul);
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Terjadi Kesalahan: " + e.getMessage());
        }
    }
    
    public ArrayList<Krs> getKrsList(String keyword){
     ArrayList<Krs> krsList = new ArrayList<Krs>();
     Koneksi koneksi =  new Koneksi();
     Connection connection = koneksi.getKoneksi();
     
     String query ="SELECT * FROM tb_krs";
     Statement st;
     ResultSet rs;
     
     try{
         st = connection.createStatement();
         rs = st.executeQuery(query);
         while (rs.next()){
             krs = new Krs(
                     rs.getString("id_krs"),
                     rs.getString("npm"),
                     rs.getString("kode_jadwal"),
                     rs.getString("tahun_ajar"),
                     rs.getString("nama"),
                     rs.getString("dosen"),
                     rs.getString("Semester"),
                     rs.getString("makul")
             );
             krsList.add(krs);
         }
     } catch (SQLException | NullPointerException ex){
         System.err.println("Koneksi Null Gagal");
     }
        return krsList;
    }
    
    public void selectMahasiswa(String keyword){
        ArrayList<Krs> list = getKrsList(keyword);
        DefaultTableModel model = (DefaultTableModel)tabeldiambil.getModel();
        Object[] row = new Object[8];
        
        for (int i = 0; i <list.size(); i++){
            row[0] = list.get(i).getIdKrs();
            row[1] = list.get(i).getNpm();
            row[2] = list.get(i).getKodeJadwal();
            row[3] = list.get(i).getMakul();
            row[4] = list.get(i).getSemester();
            row[5] = list.get(i).getDosen();
            row[6] = list.get(i).getNama();
            row[7] = list.get(i).getTahun();

            model.addRow(row);
        }
    }

    public final void resetTable(String keyword){
        DefaultTableModel model = (DefaultTableModel)tabeldiambil.getModel();
        model.setRowCount(0);
        selectMahasiswa(keyword);
    }
    
